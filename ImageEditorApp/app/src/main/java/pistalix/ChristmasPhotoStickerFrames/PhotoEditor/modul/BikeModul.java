public class BikeModul {

    private int bikethump;
    private int bikeph;

    public BikeModul(int bikethump, int bikeph) {
        this.bikethump = bikethump;
        this.bikeph = bikeph;
    }

    public int getBikethump() {
        return bikethump;
    }

    public void setBikethump(int bikethump) {
        this.bikethump = bikethump;
    }

    public int getBikeph() {
        return bikeph;
    }

    public void setBikeph(int bikeph) {
        this.bikeph = bikeph;
    }
}
